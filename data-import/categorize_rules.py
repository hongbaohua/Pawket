_LOW_CONF_KEYWORDS = ['烏內','屋內','八沅','加恩','小梅花商行','季緣','圓石','台江北','川牛木石亭','肥貓抓雞蛋脆皮雞蛋糕','波奇波奇']

def is_low_confidence(merchant):
    if not merchant:
        return False
    m = merchant.lower()
    return any(lc in m for lc in _LOW_CONF_KEYWORDS)

# 支出方向的例外：商家文字裡有「姊-」開頭且跟租屋/水電/餐費有關 -> 算 Fixed/居住房租 (透過姊姊代付房租水電)
def special_case(merchant, amount):
    if not merchant:
        return None
    if merchant.startswith('姊-') and ('租屋' in merchant or '水電' in merchant or '天然氣' in merchant or '餐費' in merchant):
        return ('Fixed', '居住房租', '')
    if merchant == '提款':
        return 'TRANSFER_CTBC_TO_CASH'
    return None

def apply_rules(merchant):
    if not merchant:
        return None
    m = merchant.lower()
    for keywords, cat in RULES:
        for kw in keywords:
            if kw.lower() in m:
                return cat
    return None
